/**
 * add - function that checks the number sign
 *
 * @a: the number to be checked
 * @b: the number to be checked
 *
 * Return: return 1 if 'n' is positive
 * otherwise  0 if 'n' is zero
 *  and -1 if 'n' is negative
 *
 */
int add(int a, int b)
{
	int sum = a + b;
	return (sum);
}
