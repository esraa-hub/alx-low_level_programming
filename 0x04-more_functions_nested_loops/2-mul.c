#include "main.h"

/**
 *  mul -function that checks the number sign
 *
 * @a: the number to be checked
 * @b: the number to be checked
 *
 * Return: return 1 if 'c' is positive
 * otherwise  0 if 'c' is zero
 *  and -1 if 'c' is negative
 *
 */
int mul(int a, int b)
{
	int result;

	result = a * b;
	return (result);
}
