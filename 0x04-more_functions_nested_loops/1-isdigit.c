/**
 *  _isupper- function that checks the number sign
 *
 * @c: the number to be checked
 *
 * Return: return 1 if 'c' is positive
 * otherwise  0 if 'c' is zero
 *  and -1 if 'c' is negative
 *
 */
int _isdigit(int c)
{
	if (c >= 48 && c <= 57)
	       return (1);
	return (0);
}
