/**
 *  *_strcpy -function that checks the number sign
 *
 *  @src : array iks kcmsdlvmkslmslm
 *  @dest : size of array
 *
  * Return: return 1 if 'c' is positive
 * otherwise  0 if 'c' is zero
 *  and -1 if 'c' is negative
 *
 */
char *_strcpy(char *dest, char *src)
{
	int i;

	for (i = 0; src[i] = '\0'; ++i)
	{
		dest[i] = src[i];
	}
	return (dest);
}
